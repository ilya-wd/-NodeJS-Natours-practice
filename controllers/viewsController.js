exports.getMyTours = catchAsync(async (req, res, next) => {
  // 1) Find all bookings - all tours/bookings that belong to a current user
  const bookings = await Booking.find({ user: req.user.id });

  // 2) Find tours with the returned IDs. Tour itself is already a tour id, so we don't need el/tour.id
  const tourIDs = bookings.map((el) => el.tour);
  // this operator selects all the tours that have an _id which is in tourIDs array
  const tours = await Tour.find({ _id: { $in: tourIDs } });

  res.status(200).render('overview', {
    title: 'My Tours',
    tours,
  });
});

exports.updateUserData = catchAsync(async (req, res, next) => {
  const updatedUser = await User.findByIdAndUpdate(
    req.user.id,
    {
      name: req.body.name,
      email: req.body.email,
    },
    {
      new: true,
      runValidators: true,
    }
  );

  res.status(200).render('account', {
    title: 'Your account',
    user: updatedUser,
  });
});
